package lesson5.task5;

public class LadderTest {

    public static void main(String[] args) {

        System.out.println("Start program");

        int a = 15;
        int b = 15;

        if (a < b){
            System.out.println("A < B");
        } else if (a > b) {
            System.out.println("A > B");
        }else {
            System.out.println("A = B");
        }

        System.out.println("Finish program");
    }
}
